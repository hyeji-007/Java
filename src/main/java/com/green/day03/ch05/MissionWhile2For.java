package com.green.day03.ch05;

public class MissionWhile2For {

    public static void main(String[] args) {

        for (int n = 1; n <= 100; n++) {
            System.out.println(n);
        }
        System.out.println("--------------------");

        for (int n = 100; n > 0; n--) {
            System.out.println(n);
        }




    }
}
