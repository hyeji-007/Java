package com.green.day03.ch05;

public class MissionWhile2 {

    public static void main(String[] args) {

        int n = 1;
        while (n <= 100) {
            System.out.println(n++);
        }
        System.out.println();

        int t = 100;
        while (t >= 1) {
            System.out.println(t--);
        }
        System.out.println();

        int g = 1;
        do {
            System.out.println(g++);
        } while (g <= 100);
        System.out.println();

        int y = 100;
        do {
            System.out.println(y--);
        } while (y >= 1);













    }
}
